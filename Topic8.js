// Debouncing & Throttling
//#Both debouncing and throttling are optimization techniques to control the execution frequency of a function, especially in scenarios like search inputs, button clicks, and scroll events.
//#Throttling ensures a function executes at most once in a specified interval, even if triggered multiple times.

// Event Delegation
//#Event Delegation is a technique where instead of adding event listeners to multiple child elements, you add a single event listener to a parent element and use event bubbling to handle events efficiently.

// Lazy Loading & Code Splitting
//#Lazy Loading is a technique where resources (images, scripts, or components) are loaded only when needed instead of at page load. This improves performance and reduces initial load time.
//#Code Splitting is a technique to split JavaScript bundles into smaller chunks, so the browser loads only what's needed for the current page.

